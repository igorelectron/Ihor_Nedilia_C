/*2.2. Обчислити гіпотенузу c прямокутного трикутника за катетами а і b*/

#include <stdio.h>
#include <math.h>

int main()
{
    float a, b;
    printf("Введіть значення катета а:\n");
    scanf("%f", &a);
    printf("Введіть значення катета b:\n");
    scanf("%f", &b);
    float c = hypot(a, b); // використовуємо математичну функцію hypot() для обчислення гіпотенузи
    printf("У прямокутному трикутнику з катетами a = %.2f та b = %.2f маємо гіпотенузу с = %.2f", a, b, c);
}
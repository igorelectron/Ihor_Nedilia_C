/* Для довільного цілого числа m ≥ 1 знайти найбільше ціле k, при якому 4^k ≤ m*/

#include <stdio.h>

int main()
{
    int k = 0, m;
    printf("Введіть ціле число m ≥ 1:\n");
    scanf("%d", &m);
    int i = 1;
    while (i * 4 <= m) // перевіряємо поточний степінь
    {
        i *= 4; // 1*4=4*4*4*4*... (моделюємо степінь k)
        k++;
    }
    printf("Шукане ціле число k, при якому 4^k ≤ m: %d", k);
}
/* Машинний нуль компілятора */

#include <stdio.h>
#include <float.h>

int main()
{
    double a = 1.;
    do
    {
        a /= 2.;
    } while (1. != 1. + a);
    printf("Машинний нуль компілятора gcc:\n%.18lf", a); // виводимо якомога більше чисел після коми, щоб побачити машинний нуль
}